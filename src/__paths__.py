import os

# ----------------------------------------------------------------
# EXPORTS
# ----------------------------------------------------------------

__all__ = [
    "get_root_path",
    "get_source_path",
]

# ----------------------------------------------------------------
# CONSTANTS
# ----------------------------------------------------------------

_path = os.path.dirname(__file__)
_root = os.path.dirname(_path)

# ----------------------------------------------------------------
# METHODS
# ----------------------------------------------------------------


def get_source_path() -> str:
    return _path


def get_root_path() -> str:
    return _root
